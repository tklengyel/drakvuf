#ifndef PROCDUMP_H
#define PROCDUMP_H

#include <map>
#include <unordered_map>
#include <string>

#include <libvmi/libvmi.h>

#include "plugins/plugins_ex.h"
#include "plugins/private.h"

struct procdump_config
{
    const char* procdump_dir;
    bool compress_procdumps;
    std::shared_ptr<std::unordered_map<vmi_pid_t, bool>> terminated_processes;
};

enum pool_status
{
    POOL_INVALID,
    POOL_FREE,
    POOL_USED,
};
using pool_status_t = pool_status;
using pool_map_t = std::map<addr_t, pool_status_t>;

// Use second field to store thread ID. Zero TID marks finished process.
using terminating_map_t = std::map<vmi_pid_t, uint32_t>;

class procdump : public pluginex
{
public:
    std::shared_ptr<std::unordered_map<vmi_pid_t, bool>> terminated_processes;
    std::string const procdump_dir;
    bool const use_compression;
    // FIXME Base class `pluginex` contains member `traps`
    GSList* traps;
    uint64_t procdumps_count;
    pool_map_t pools;
    terminating_map_t terminating;

    addr_t malloc_va;
    addr_t memcpy_va;
    addr_t clean_process_va;

    uint16_t win_build_number;
    uint16_t win_major;
    uint16_t win_minor;
    uint32_t num_cpus;
    std::array<uint32_t, 3> vendor;
    uint32_t version_information;
    uint32_t feature_information;
    uint32_t amd_extended_cpu_features;

    procdump(drakvuf_t drakvuf, const procdump_config* config, output_format_t output);
    ~procdump();

    bool stop();
};

#endif
